export async function fetchLatestInvoices() {
  try {
    await dbConnect();
    const data = await Invoice.find().sort({ _id: -1 }).limit(5);

    const latestInvoices = data.map((invoice) => ({
      ...invoice,
      amount: formatCurrency(invoice.amount),
    }));
    return latestInvoices;
  } catch (error) {
    console.error("Database Error:", error);
    throw new Error("Failed to fetch the latest invoices.");
  }
}

export async function fetchCardData() {
  try {
    // You can probably combine these into a single SQL query
    // However, we are intentionally splitting them to demonstrate
    // how to initialize multiple queries in parallel with JS.
    await dbConnect();
    const invoiceCountPromise = Invoice.countDocuments();
    const customerCountPromise = Customer.countDocuments();
    const invoiceStatusPromise = Invoice.aggregate([
      {
        $group: {
          _id: "$status",
          total: {
            $sum: "$amount",
          },
        },
      },
    ]);

    const data = await Promise.all([
      invoiceCountPromise,
      customerCountPromise,
      invoiceStatusPromise,
    ]);

    const numberOfInvoices = Number(data[0] ?? "0");
    const numberOfCustomers = Number(data[1] ?? "0");
    const totalPaidInvoices = formatCurrency(data[2][1].total ?? "0");
    const totalPendingInvoices = formatCurrency(data[2][0].total ?? "0");

    return {
      numberOfCustomers,
      numberOfInvoices,
      totalPaidInvoices,
      totalPendingInvoices,
    };
  } catch (error) {
    console.error("Database Error:", error);
    throw new Error("Failed to fetch card data.");
  }
}

const ITEMS_PER_PAGE = 6;
export async function fetchFilteredInvoices(
  query: string,
  currentPage: number
) {
  const offset = (currentPage - 1) * ITEMS_PER_PAGE;

  try {
    await dbConnect();
    const invoices = await Invoice.find({
      $or: [{ status: new RegExp(query, "i") }],
    })
      .sort({ date: -1 })
      .skip(offset)
      .limit(ITEMS_PER_PAGE);

    return invoices;
  } catch (error) {
    console.error("Database Error:", error);
    throw new Error("Failed to fetch invoices.");
  }
}

export async function fetchInvoicesPages(query: string) {
  try {
    const count = await Invoice.countDocuments({
      $or: [{ status: new RegExp(query, "i") }],
    });
    const totalPages = Math.ceil(Number(count) / ITEMS_PER_PAGE);
    return totalPages;
  } catch (error) {
    console.error("Database Error:", error);
    throw new Error("Failed to fetch total number of invoices.");
  }
}

export async function fetchInvoiceById(id: string) {
  try {
    await dbConnect();
    const data = await Invoice.find({ id: id });

    const invoice = data.map((invoice) => ({
      ...invoice,
      // Convert amount from cents to dollars
      amount: invoice.amount / 100,
    }));

    return invoice[0];
  } catch (error) {
    console.error("Database Error:", error);
    throw new Error("Failed to fetch invoice.");
  }
}

